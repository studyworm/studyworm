-- D-1 vs STD (배치 실행 결과)
SELECT
	SND_YEAR,
	SND_MN,
	SND_BRN_CD,
	SND_WHS_CD,
	REQ_BRN_CD,
	REQ_WHS_CD,
	count(*) AS ROW_CNT,
	sum(ALLO_QTY) AS  SUM_ALLO_QTY 
FROM
	MBSASSTD.TBSITUTRF UTRF_STD
WHERE
	UTRF_STD.SND_NO NOT IN (
	SELECT
		SND_NO
	FROM
		MBSASMDTBAK.TBSITUTRF)
GROUP BY
	SND_YEAR,
	SND_MN,
	SND_BRN_CD,
	SND_WHS_CD,
	REQ_BRN_CD,
	REQ_WHS_CD;

